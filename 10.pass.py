# pass_example.py

# 1. Example using pass in a loop
for i in range(5):
    pass  # এই লুপটি কিছুই করবে না, শুধুমাত্র pass স্টেটমেন্ট থাকবে

# 2. Example using pass in a function
def my_function():
    pass  # এই ফাংশনটি কোনো কাজ করবে না, শুধুমাত্র pass স্টেটমেন্ট থাকবে

# 3. Example using pass in a class
class MyClass:
    pass  # এই ক্লাসটি খালি থাকবে, শুধুমাত্র pass স্টেটমেন্ট থাকবে

# 4. Example using pass in an if statement
x = 10
if x > 5:
    pass  # যদি x ৫ এর বেশি হয়, তাহলে কিছুই হবে না, শুধু pass হবে

print("Code executed successfully!")
